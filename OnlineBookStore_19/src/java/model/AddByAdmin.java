package model;

/**
 *
 * @author MiNNiE
 */
public class AddByAdmin {
 
    private String info;
    private String id;
    
    public AddByAdmin(String info, String id)
    {
    this.id=id;
    this.info=info;
    
    }

    /**
     * @return the info
     */
    public String getInfo() {
        return info;
    }

    /**
     * @param info the info to set
     */
    public void setInfo(String info) {
        this.info = info;
    }

    /**
     * @return the id
     */
    public String getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(String id) {
        this.id = id;
    }
    
    @Override
    public String toString() {
        return info+" @id="+id;
    }
    
    
}
